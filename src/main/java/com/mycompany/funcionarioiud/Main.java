    private void setModelo() {
        String[] cabecera = {"Id", "Tipo documento", "Numero documento", "Nombres",
            "Apellidos", "Estado Civil", "Sexo", "Dirección", "Telefono", "Fecha Nacimiento"};
        table1Funcionarios.setColumnIdentifiers(cabecera);
        tablaFuncionarios.setModel(table1Funcionarios);
    }

    private void setDatos() {
        try {
            List<Funcionario> func = funcControl.obtenerFuncionarios();

            Object[] datos = new Object[table1Funcionarios.getColumnCount()];
            table1Funcionarios.setRowCount(0);
            func.forEach(f -> {
                datos[0] = f.getIdFuncionarid();
                datos[1] = f.getTipoIdenti();
                datos[2] = f.getNumeroIdent();
                datos[3] = f.getNombre();
                datos[4] = f.getApellido();
                datos[5] = f.getEstadoCivil();
                datos[6] = f.getSexo();
                datos[7] = f.getDireccion();
                datos[8] = f.getTelefono();
                datos[9] = f.getFechaNaciomiento();

                table1Funcionarios.addRow(datos);
            });
            tablaFuncionarios.setModel(table1Funcionarios);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

    public void setDaticos() {
        try {
            List<Funcionario> func = funcControl.obtenerFuncionarios();

            if (func.isEmpty()) {
                System.out.println("No hay funcionarios");
                seleccionFunc.addItem("No hay funcionarios");
            } else {
                seleccionFunc.removeAllItems();
                seleccionDeleteFunc.removeAllItems();
                //seleccionFunc.setSelectedItem(null);
                func.forEach(f -> {
                    seleccionFunc.addItem("ID: " + String.valueOf(f.getIdFuncionarid()) + " Nombre: " + f.getNombre());
                    seleccionDeleteFunc.addItem("ID: " + String.valueOf(f.getIdFuncionarid()) + " Nombre: " + f.getNombre());
                });
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

