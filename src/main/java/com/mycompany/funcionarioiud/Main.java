    private void delete_buttonDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delete_buttonDeleteMouseClicked
        // TODO add your handling code here:
        try {

            String almacen = seleccionDeleteFunc.getSelectedItem().toString();
            String[] idString = almacen.split(" ");
            int id = Integer.parseInt(idString[1]);
            Funcionario funcExiste = funcControl.obtenerFuncionario(id);
            if (funcExiste == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "ALERTA\nLa ID del funcionario\nNo Existe", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                System.out.println("La Id del funcionario no existe");

                return;
            }

            funcControl.eliminarFuncionario(id);
            System.out.println("Funcionario Eliminado exitosamente");
            javax.swing.JOptionPane.showMessageDialog(this, "El funcionario:\n se ha eliminado correctamente\n", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            setDatos();
            setDaticos();

        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "El funcionario:\n No se ha borrado\nLos funcionarios que estan relacionados a la tabla grupo_familiar\n"
                    + "no deja borrarlos porque la tabla familiares esta relacionada a la tabla grupo_familiar\n"
                    + "Por favor intente crear un nuevo funcionario para poder probar la funcionalidad de eliminar", "Error", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_delete_buttonDeleteMouseClicked

