                edit_estado_civil.setForeground(Color.black);
                edit_sexo.setText(func.getSexo());
                edit_sexo.setForeground(Color.black);
                edit_direccion.setText(func.getDireccion());
                edit_direccion.setForeground(Color.black);
                edit_telefono.setText(String.valueOf(func.getTelefono()));
                edit_telefono.setForeground(Color.black);
